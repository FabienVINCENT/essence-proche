export const getStationById = (id: number, code_departement: string) => {
    // On supprime le 0 devant le code département si il y en a un
    if (code_departement[0] === '0') {
        code_departement = code_departement.substring(1)
    }
    // @ts-ignore
    const {stations} = allStations[parseInt(code_departement)]

    // On récupère la station correspondant à l'id
    const station = stations.find((station: any) => parseInt(station.id) === id)

    return station
}

